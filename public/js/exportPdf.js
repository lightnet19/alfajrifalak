export async function exportRukyatPDF(data) {
  const { jsPDF } = window.jspdf || {};
  if (!jsPDF) {
    alert("Library PDF belum dimuat.");
    return;
  }

  const doc = new jsPDF();
  const r = data.raw;
  let y = 16;

  const line = () => {
    doc.line(14, y, 196, y);
    y += 6;
  };

  const title = (t) => {
    doc.setFont("helvetica", "bold");
    doc.setFontSize(13);
    doc.text(t, 14, y);
    y += 6;
  };

  const row = (label, value) => {
    doc.setFont("helvetica", "normal");
    doc.setFontSize(9.5);
    doc.text(label, 14, y);
    doc.text(`: ${value}`, 72, y);
    y += 5.2;
  };

  doc.setFont("helvetica", "bold");
  doc.setFontSize(16);
  doc.text("LAPORAN RUKYAT HILAL", 105, y, { align: "center" });
  y += 7;
  doc.setFontSize(10);
  doc.text("Sistem Alfajri", 105, y, { align: "center" });
  y += 5;
  doc.text("oleh Fuad Baidlowi Al-Fajri", 105, y, { align: "center" });
  y += 8;

  line();

  title("A. Identitas Markaz");
  row("Markaz", data.markaz || "-");
  row("Lintang", num(data.lat));
  row("Bujur", num(data.lon));
  row("Elevasi", `${data.elevation || 0} mdpl`);
  row("Zona Waktu", data.tz ?? 7);
  line();

  title("B. Data Ijtima");
  row("Tanggal Ijtima", r.ijtima ? r.ijtima.toLocaleString() : "-");
  row("Julian Date", num(r.JD, 6));
  row("Delta T", `${num(r.deltaT)} s`);
  row("JD Ephemeris", num(r.JDE, 6));
  line();

  title("C. Data Matahari & Hilal");
  row("Sunset", formatTime(r.sunset));
  row("Altitude Matahari", `${num(r.sunAltitude)}°`);
  row("Azimuth Matahari", `${num(r.sunAzimuth)}°`);
  row("Altitude Hilal", `${num(r.apparentAltitude)}°`);
  row("Azimuth Hilal", `${num(r.moonAzimuth)}°`);
  row("Elongasi", `${num(r.elongation)}°`);
  line();

  title("D. Parameter Hilal");
  row("Refraction", `${num(r.refraction)}°`);
  row("Semi Diameter", `${num(r.semiDiameter)}°`);
  row("Illuminasi", `${illumination(r)} %`);
  row("Lebar Hilal", `${width(r)}°`);
  row("Umur Hilal", `${num(r.moonAgeHours)} jam`);
  row("Moon Lag", `${num(r.moonLagMinutes)} menit`);
  row("Best Time Rukyat", r.bestTime ? r.bestTime.toLocaleTimeString() : "-");
  line();

  title("E. Koordinat & Koreksi");
  row("Topocentric Ecliptic λ", `${num(r.topocentricEcliptic?.lambdaTopo)}°`);
  row("Topocentric Ecliptic β", `${num(r.topocentricEcliptic?.betaTopo)}°`);
  row("True Obliquity", `${num(r.epsilon)}°`);
  row("Jarak Bumi-Bulan", `${num(r.moonDistanceKm)} km`);
  line();

  title("F. Visibilitas Internasional");
  row("Yallop", r.yallop?.category || "-");
  row("Odeh", r.odeh?.category || "-");
  line();

  title("G. Kriteria Indonesia");
  row("MABIMS", r.mabims?.memenuhi ? "Memenuhi" : "Tidak");
  row("IRNU (NU)", r.irnu?.memenuhi ? "Memenuhi" : "Tidak");
  row("Muhammadiyah", r.muhammadiyah?.memenuhi ? "Memenuhi" : "Tidak");
  line();

  title("H. Kesimpulan");
  doc.setFont("helvetica", "bold");
  doc.setFontSize(10);
  doc.text(
    r.decision ? "Hilal Layak sebagai Awal Bulan Hijriyah" : "Hilal Belum Layak (Istikmal 30 Hari)",
    14,
    y
  );
  y += 10;
  line();

  title("I. Pengesahan");
  doc.setFont("helvetica", "normal");
  row("Mengetahui", "Lembaga Falakiyah PCNU Kencong");
  row("Nama", "__________________________");
  row("Jabatan", "Ketua LF PCNU Kencong");
  row("Tanggal", "__________________________");

  doc.setFontSize(8);
  doc.text("Generated by Alfajri — Sistem Rukyat Digital", 14, 285);

  doc.save(`laporan-rukyat-${data.date}.pdf`);
}

function num(v, digits = 2) {
  return Number.isFinite(v) ? v.toFixed(digits) : "-";
}

function formatTime(v) {
  if (v == null || Number.isNaN(v)) return "-";
  const h = Math.floor(v);
  const m = Math.floor((v - h) * 60);
  return `${String(h).padStart(2, "0")}:${String(m).padStart(2, "0")}`;
}

function illumination(r) {
  return (((1 - Math.cos((r.elongation || 0) * Math.PI / 180)) / 2) * 100).toFixed(2);
}

function width(r) {
  const sd = r.semiDiameter || 0;
  return (sd * (1 - Math.cos((r.elongation || 0) * Math.PI / 180))).toFixed(4);
}
